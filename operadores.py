# Operações Aritimeticas

"""
soma: =
subtracao: -
multiplicacao: *
divisao: /
divisao inteira: //
resto da divisao: %
potencia: **
"""

num1 = 10
num2 = 20

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 // num2)
print(20 % 3)
print(2 ** 3)

